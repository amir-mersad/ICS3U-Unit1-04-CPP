// This program calculates the area and the perimeter of a rectangle
//     with dimensions 5cm x 3cm

#include <iostream>

int main() {
    std::cout << "If a rectangle has the dimensions:" << std::endl;
    std::cout << "5cm x 3cm" << std::endl;
    std::cout << "" << std::endl;
    std::cout << "Area is "<< (3*5) << "cm^2" << std::endl;
    std::cout << "Perimeter is "<< (2*(3+5)) << "cm" << std::endl;
}
